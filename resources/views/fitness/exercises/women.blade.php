                    <h2>Triceps: Swiss Ball Lying Triceps Extension
                    </h2>

                    <p><strong>The benefit:</strong> Lying on a Swiss ball forces your core to work harder to keep you stable. So you work your abs as you shape the backs of your arms.
                      </p>
                    <p>
                        <strong>How to do it:</strong> Grab a pair of dumbbells and lie on your back on a Swiss ball that your middle and upper back are on the ball. Raise your hips so your body forms a straight line from your knees to your shoulders. Hold the dumbbells over your forehead, with your arms straight and your palms facing each other [A]. Without moving your upper arms, bend your elbows and lower the dumbbells until your forearms are beyond parallel to the floor [B]. Pause, then lift the weights back to the start.

                      

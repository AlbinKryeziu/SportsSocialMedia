                    <h2>Quadriceps: Offset Dumbbell Lunge
                    </h2>

                    <p><strong>The benefit:</strong> Holding a weight on just one side of your body increases the demand placed on your core to keep your body stable. The result: Your hips and abs have to work harder, and you'll also improve your balance. And better yet, you'll burn tons of calories.</p>
                    <p>
                        <strong>How to do it:</strong> Hold a dumbbell in your right hand next to your shoulder, with your arm bent [A]. Step forward with your right leg and lower your body until your right knee is bent at least 90 degrees and your left knee nearly touches the floor [B]. Push yourself back to the starting position. That's one rep. Do all your reps, then repeat with your left leg, while holding the weight in your left hand.

                    </p>

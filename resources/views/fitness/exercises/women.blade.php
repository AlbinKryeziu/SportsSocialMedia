                    <h2>Hip Raise</h2>

                    <p><strong>The benefit:</strong> It targets the muscles of your rear end, which can help make your belly flatter. The reason: When your glutes are weak—as they are in most women—the top of your pelvis tilts forward. This not only places stress on your lower back, but it causes your tummy to stick out—even if you don't have an ounce of fat. Your fix: the hip raise.</p>
                    <p>
                        <strong>How to do it:</strong> Lie on your back on the floor with your knees bent and your feet flat on the floor [A]. Now brace your core, squeeze your glutes, and raise your hips so your body forms a straight line from your shoulders to your knees [B]. Pause for 3 to 5 seconds—squeezing your glutes tightly the entire time—then lower back to the start.

                    </p>

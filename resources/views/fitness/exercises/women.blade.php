                    <h2>Hamstrings: Single-Leg Dumbbell Straight-Leg Deadlift
                    </h2>

                    <p><strong>The benefit:</strong> Besides targeting your hamstrings, this exercise works your glutes and core. It also helps eliminate muscle imbalances between your legs, reducing your risk of injury. And as a bonus, it can improve the flexibility of your hamstrings, because it stretches these muscles every time you lower the weight.
                      </p>
                    <p>
                        <strong>How to do it:</strong> Grab a pair of dumbbells with an overhand grip and hold them at arm's length in front of your thighs. Stand with your feet shoulder-width apart and knees slightly bent. Now raise one leg off the floor [A]. Without changing the bend in your knee, keep your back naturally arched, bend at your hips, and lower your torso until it's almost parallel to the floor [B]. Pause, then squeeze your glutes, thrust your hips forward, and raise your torso back to the starting position.

                    </p>

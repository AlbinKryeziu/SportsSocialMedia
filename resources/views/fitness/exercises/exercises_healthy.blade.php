                    <h2>Doing the exercises</h2>

                    <img class="img-fluid rounded" src="{{ asset('images/other/sm.jpeg') }}" style="width: 900px; object-fit: cover;" alt="" />

                    <hr />

                    <p>
                        People can do the following exercises individually or as part of a circuit. Some require basic fitness equipment, such as dumbbells or an exercise ball, but people can do many of them with no equipment. The American
                        Council on Exercise recommend that people continue doing repetitions until they reach muscle fatigue or can no longer maintain proper form.
                        <br />
                        <br />
                        However, they note that endurance athletes, such as runners and cyclists, should stick to about 20–30 repetitions rather than working to the point of muscle fatigue. The American College of Sports Medicine (ACSM)
                        recommend doing 8–12 repetitions of 8–10 strength training exercises on at least 2 days of the week.
                        <br />
                        <br />
                        The ACSM also recommend at least 30 minutes of moderate intensity physical activity a day, 5 days per week. Alternatively, they say that people can perform 20-minute sessions of vigorous physical activity on 3 days

                    <h2>Pushups</h2>

                    <img class="img-fluid rounded" src="{{ asset('images/other/gift2.gif') }}" style="width: 900px; object-fit: cover;" alt="" />

                    <hr />

                   <p>Pushups work multiple muscle groups, strengthening the arms, chest, and shoulders.</p>
                   <p>How to do them:</p>
                   <p>
                       <ul>
                           <li>Start in a plank position with the arms straight and the body lifted in a straight line horizontal to the floor. Keep the feet together and the toes flexed to support the body.</li>
                           <br>
                           
                           <li>The palms should be flat on the floor shoulder-width apart, with the fingers facing straight ahead or slightly inward.</li>
                           <br>
                           
                           <li>Lower as far as possible, aiming to touch the chest or chin to the floor.</li>
                           <br>
                           
                           <li>Keep the abdominal muscles engaged throughout to help support the back.</li>
                       </ul>
                   </p>
                   <hr>
                   <h2>Bodyweight squats</h2>

                    <img class="img-fluid rounded" src="{{ asset('images/other/gif3.gif') }}" style="width: 900px; object-fit: cover;" alt="" />

                    <hr />

                   <p>Bodyweight squats can increase lower body and core strength as they work the abs, buttocks, hips, thighs, calves, and shins.</p>
                   <p>How to do them:</p>
                   <p>
                       <ul>
                           <li>Stand with the feet slightly wider than hip-width apart, angling the toes slightly outward</li>
                           <br>
                           
                           <li>Keep the hands down by the sides, with the palms facing in and keep the shoulders back.</li>
                           <br>
                           
                           <li>Push through the feet to straighten back up into the starting position.</li>
                           <br>
                           
                           <li>Inhale into the squat, then exhale when standing back up. </li>
                       </ul>
                   </p>
                   <hr>
                   <h2>Lunges</h2>

                   <img class="img-fluid rounded" src="{{ asset('images/other/gif4.gif') }}" style="width: 900px; object-fit: cover;" alt="" />

                   <hr />

                  <p>Lunges work the thighs, buttocks, hips, and abdominal muscles.</p>
                  <p>How to do them:</p>
                  <p>
                      <ul>
                          <li>Stand upright with the feet together.</li>
                          <br>
                          
                          <li>Step one leg forward into a long stride, bending the knee and placing the foot flat on the floor.</li>
                          <br>
                          
                          <li>Bend the knee of the supporting leg toward the floor.</li>
                          <br>
                          
                          <li>Use the muscles of the forward leg to push back to standing.</li>
                      </ul>
                  </p>
                  <hr>
                  <h2>Side planks</h2>

                  <img class="img-fluid rounded" src="{{ asset('images/other/gif5.gif') }}" style="width: 900px; object-fit: cover;" alt="" />

                  <hr />

                 <p>Side planks help build core strength, which can help reduce lower back pain. Side planks work the buttocks, hips, and abdominal muscles.</p>
                 <p>How to do them:</p>
                 <p>
                     <ul>
                         <li>Lie on the right side with the legs outstretched directly on top of each other and the elbow under the shoulder on the right arm.</li>
                         <br>
                         
                         <li>Engage the abdominals and lift the knees and hips off the floor, keeping the head and body aligned.</li>
                         <br>
                         
                         <li>Slowly return to the floor, switch to the left side, and repeat.</li>
                         <br>
                         
                        
                     </ul>
                 </p>
                 <hr>
                 <h2>Planks</h2>

                 <img class="img-fluid rounded" src="{{ asset('images/other/gif6.gif') }}" style="width: 900px; object-fit: cover;" alt="" />

                 <hr />

                <p>Planks strengthen the back and abdominal muscles and help build core strength.</p>
                <p>How to do them:</p>
                <p>
                    <ul>
                        <li>Start with the elbows and lower arms on the floor, keeping the elbows in line with the shoulders.</li>
                        <br>
                        
                        <li>Lift the body so that it forms a straight line horizontal to the floor.</li>
                        <br>
                        
                        <li>Keep the feet together and the toes flexed to support the body.</li>
                        <br>
                        
                       
                    </ul>
                </p>
                <p>Once people feel strong performing this exercise, they can try a high plank. This move uses the same body positioning, but the person keeps their arms straight with their palms flat on the floor, directly underneath the shoulders.</p>

                <hr>
                <h2>Knee tucks</h2>

                <img class="img-fluid rounded" src="{{ asset('images/other/gif7.gif') }}" style="width: 900px; object-fit: cover;" alt="" />

                <hr />

               <p>Knee tucks work the abdominals, calves, and shins. People will need an exercise ball, sometimes called a stability ball, for this exercise.</p>
               <p>How to do them:</p>
               <p>
                   <ul>
                       <li>Lie on the stomach on top of the stability ball with the hands and feet on the floor.</li>
                       <br>
                       
                       <li>Walk forward on the hands until the knees are resting on the ball and the feet have lifted off the floor. The hands should be directly underneath the shoulders.</li>
                       <br>
                       
                       <li>Slowly push the knees back to return to the starting position.</li>
                       <br>
                       
                      
                   </ul>
               </p>
               <hr>
               <h2>Glute bridge</h2>

               <img class="img-fluid rounded" src="{{ asset('images/other/gif8.gif') }}" style="width: 900px; object-fit: cover;" alt="" />

               <hr />

              <p>The glute bridge is good for the muscles in the back of the body, known as the posterior chai</p>
              <p>How to do them:</p>
              <p>
                  <ul>
                      <li>Lie on the back with the knees bent and the feet flat on the floor.</li>
                      <br>
                      
                      <li>Contract the buttocks and abdominals to lift the hips off the floor, bringing them in line with the shoulders and knees. Avoid arching the lower back.</li>
                      <br>
                      
                      <li>Slowly lower back to the starting position..</li>
                      <br>
                      
                     
                  </ul>
              </p>
              <hr>
              <h2>Standing overhead dumbbell presses</h2>

              <img class="img-fluid rounded" src="{{ asset('images/other/gif9.gif') }}" style="width: 900px; object-fit: cover;" alt="" />

              <hr />

             <p>This exercise helps strengthen the shoulder muscles. People will require two dumbbells.</p>
             <p>How to do them:</p>
             <p>
                 <ul>
                     <li>Stand with the feet hip-width apart</li>
                     <br>
                     
                     <li>Hold a dumbbell in each hand with the inside of the wrists facing forward, then bend the arms to bring the weights to shoulder height.</li>
                     <br>
                     
                     <li>Engage the abdominal muscles and exhale while extending the arms straight up to lift the dumbbells in a straight line above the shoulders.</li>
                     <br>
                     
                    
                 </ul>
             </p>

                    <p>
                        "Healthy living" to most people means both physical and mental health are in balance or functioning well together in a person. In many instances, physical and mental health are closely linked, so that a change (good or bad) in one directly affects the other. Consequently, some of the tips will include suggestions for emotional and mental "healthy living."
                    </p>
                    <hr />
                    <h2>Healthy eating (diet and nutrition)</h2>

                   <p>All humans have to eat food for growth and maintenance of a healthy body, but we humans have different nutrition requirements as infants, children (kids), teenagers, young adults, adults, and seniors. For example, infants may require feeding every 4 hours until they gradually age and begin to take in more solid foods. Eventually they develop into the more normal pattern of eating three times per day as young kids. However, as most parents know, kids, teenagers, and young adults often snack between meals. Snacking is often not limited to these age groups because adults and seniors often do the same.</p>
                   <strong>Tips:</strong>
                     <p>
                         <ul>
                             <li>Eat three healthy meals a day (breakfast, lunch, and dinner); it is important to remember that dinner does not have to be the largest meal.</li>
                             <br>
                             <li>The bulk of food consumption should consist of healthy foods, such as fruits, vegetables, whole grains, and fat-free or low-fat milk products.</li>
                             <br>
                             <li>Incorporate lean meats, poultry, fish, beans, eggs, and nuts (with emphasis on beans and nuts) into a healthy diet.</li>
                             <br>
                             <li>Control portion sizes; eat the smallest portion that can satisfy hunger and then stop eating.</li>
                             <br>
                             <li>Avoid eating a large meal before sleeping to decrease gastroesophageal reflux and weight gain.</li>
                             <br>
                             <li>Avoid rewarding children with sugary snacks; such a pattern may become a lifelong habit for people.</li>
                             <br>
                             <li>Avoid heavy meals in the summer months, especially during hot days.</li>
                             <br>
                             <li>Avoid eating raw or undercooked meats of any type.</li>

                         </ul>
                     </p>
                    <hr />
                    <h2>Physical activity and exercise</h2>

                    <img class="img-fluid rounded" src="{{ asset('images/other/life4.jpeg') }}" style="width: 900px; object-fit: cover;" alt="" />

                    <hr />

                    <p>
                        Physical activity and exercise is a major contributor to a healthy lifestyle; people are made to use their bodies, and disuse leads to unhealthy living. Unhealthy living may manifest itself in obesity, weakness, lack of endurance, and overall poor health that may foster disease development.
                    </p>
                    <p><strong>Tips:</strong></p>
                    <p><ul>
                        <li>Regular exercise can prevent and reverse age-related decreases in muscle mass and strength, improve balance, flexibility, and endurance, and decrease the risk of falls in the elderly. Regular exercise can help prevent coronary heart disease, stroke, diabetes, obesity, and high blood pressure. Regular, weight-bearing exercise can also help prevent osteoporosis by building bone strength.</li>
                         <br>
                         <li>Regular fitness can help chronic arthritis sufferers improve their capacity to perform daily activities such as driving, climbing stairs, and opening jars.</li>
                         <br>
                         <li>Regular exercise can help increase self-esteem and self-confidence, decrease stress and anxiety, enhance mood, and improve general mental health.</li>
                         <br>
                         <li>Regular exercise can help control body weight and in some people cause loss of fat.</li>
                         <br>
                         <li>Thirty minutes of modest exercise (walking is OK) at least 3 to 5 days a week is recommended, but the greatest health benefits come from exercising most days of the week.</li>
                         <br>
                         <p>People are never too old to start exercising. Even frail, elderly individuals (70-90 years of age) can improve their strength and balance with exercise.</p>
                         <br>
                    </ul></p>
                    <hr />

                    <h2>Mental health</h2>

                    <img class="img-fluid rounded" src="{{ asset('images/other/life6.jpg') }}" style="width: 900px; object-fit: cover;" alt="" />

                    <hr />

                    <p>
                        Healthy living involves more than physical health, it also includes emotional or mental health. The following are some ways people can support their mental health and well-being.
                    </p>
                    <p><strong>Tips:</strong></p>
                    <p><ul>
                          <li>Get enough sleep daily; the CDC recommends the following by age group (naps inclusive); 12-18 hours from birth to 2 months, 14-15 hours from 3-11 months of age, 12-18 hours for 1-3 years of age, 11-13 hours for 3-5 years of age, 10-11 hours for 5-10 years of age, 8.5-9.5 hours for 10-17 years of age and those 18 and above need 7-9 hours of sleep. Elderly people need about 7-9 hours but do not sleep as deeply and may awaken at night or wake early, so naps (like kids need) allow them to accumulate the total of 7-9 hours of sleep.</li>   
                          <br>
                          <li>Take a walk and reflect on what you see and hear at least several times per week.</li> 
                          <br>
                          <li>Try something new and often (eat a new food, try a different route to work, go to a new museum display).</li>
                          <br>
                          <li>Do some mind exercises (read, do a puzzle occasionally during the week).</li>
                          <br>
                          <li>Try to focus on a process intensely and complete a segment of it over 1 to several hours, then take a break and do something relaxing (walk, exercise, short nap).</li>
                          <br>

                          <li>Have fun (go on a trip with someone you love, go shopping, go fishing; do not let vacation time slip away).</li>
                    </ul></p>
